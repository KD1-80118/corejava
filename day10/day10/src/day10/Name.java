package day10;

import java.util.Scanner;

public class Name {
	
	private int i;
	private String string;
	private double d;

	

	public Name(int i, String string, double d) {
		super();
		this.i = i;
		this.string = string;
		this.d = d;
	}



	public Name() {
		super();
	}



	public int getI() {
		return i;
	}



	public void setI(int i) {
		this.i = i;
	}



	public String getString() {
		return string;
	}



	public void setString(String string) {
		this.string = string;
	}



	public double getD() {
		return d;
	}



	public void setD(double d) {
		this.d = d;
	}
	
	

	
}
